/* core1_boot.S — CPU1 trampoline entry for Zynq AMP (Phase 9.5 task I).
 *
 * BootROM behavior: when CPU1 reset is released via SLCR.A9_CPU_RST_CTRL,
 * CPU1's reset vector lands in BootROM stub which polls 0xFFFFFFF0 and
 * branches there.  CPU0 must have written &core1_asm_entry to that cell
 * before clearing the reset bit.
 *
 * State at entry: MMU OFF, caches OFF, no stack, supervisor mode.
 * Need to: (1) set TTBR0 to shared MMU table, (2) enable MMU + I/D cache,
 * (3) set up stack, (4) branch to core1_main (C).
 *
 * We keep the tail-handoff trivial — main thread state is shared with
 * CPU0 via DDR; we only need basic memory + cache to call C.
 */

    .section .text.core1_boot, "ax"
    .arm
    .global core1_asm_entry
    .global _stack_core1_top      /* defined in lscript.ld */

/* External symbols from linked image (CPU0 has already set these up).
 * We use the same MMUTable that CPU0 built. */
    .extern MMUTable
    .extern core1_main

core1_asm_entry:
    /* ===== 1. Disable IRQ/FIQ, switch to SVC mode ===== */
    cpsid   if
    mrs     r0, cpsr
    bic     r0, r0, #0x1F
    orr     r0, r0, #0x13           /* SVC mode */
    msr     cpsr_c, r0

    /* ===== 2. Invalidate caches and TLBs ===== */
    mov     r0, #0
    mcr     p15, 0, r0, c8, c7, 0   /* invalidate TLBs */
    mcr     p15, 0, r0, c7, c5, 0   /* invalidate icache */
    mcr     p15, 0, r0, c7, c5, 6   /* invalidate branch predictor */
    isb

    /* Invalidate D-cache (CPU1 only — leave CPU0's alone)
     * Use simple "by-set/way" loop; conservative for L1 only. */
    mrc     p15, 1, r0, c0, c0, 1   /* CLIDR */
    ands    r3, r0, #0x7000000
    mov     r3, r3, lsr #23
    beq     dc_done_c1
    mov     r10, #0
loop1_c1:
    add     r2, r10, r10, lsr #1
    mov     r1, r0, lsr r2
    and     r1, r1, #7
    cmp     r1, #2
    blt     skip_c1
    mcr     p15, 2, r10, c0, c0, 0
    isb
    mrc     p15, 1, r1, c0, c0, 0
    and     r2, r1, #7
    add     r2, r2, #4
    ldr     r4, =0x3ff
    ands    r4, r4, r1, lsr #3
    clz     r5, r4
    mov     r9, r4
loop2_c1:
    ldr     r7, =0x7fff
    and     r7, r7, r1, lsr #13
loop3_c1:
    orr     r11, r10, r9, lsl r5
    orr     r11, r11, r7, lsl #5    /* line position */
    mcr     p15, 0, r11, c7, c6, 2  /* invalidate by set/way */
    subs    r7, r7, #1
    bge     loop3_c1
    subs    r9, r9, #1
    bge     loop2_c1
skip_c1:
    add     r10, r10, #2
    cmp     r3, r10
    bgt     loop1_c1
dc_done_c1:
    dsb

    /* ===== 3. Set VBAR to shared vector table ===== */
    ldr     r0, =_vector_table
    mcr     p15, 0, r0, c12, c0, 0
    isb

    /* ===== 4. Configure MMU: load TTBR0 with MMUTable address ===== */
    /* DACR: domain 0 = client */
    ldr     r0, =0x55555555
    mcr     p15, 0, r0, c3, c0, 0   /* DACR */
    isb

    ldr     r0, =MMUTable
    mcr     p15, 0, r0, c2, c0, 0   /* TTBR0 */
    /* TTBCR = 0 (use TTBR0 only) */
    mov     r0, #0
    mcr     p15, 0, r0, c2, c0, 2
    isb

    /* ===== 5. Enable MMU + I-cache + D-cache + branch prediction ===== */
    mrc     p15, 0, r0, c1, c0, 0   /* SCTLR */
    orr     r0, r0, #(1 << 0)        /* M: MMU enable */
    orr     r0, r0, #(1 << 2)        /* C: D-cache enable */
    orr     r0, r0, #(1 << 12)       /* I: I-cache enable */
    orr     r0, r0, #(1 << 11)       /* Z: branch prediction */
    mcr     p15, 0, r0, c1, c0, 0
    isb
    dsb

    /* ===== 6. Set up stack pointer (SVC mode) ===== */
    ldr     sp, =_stack_core1_top
    /* Align to 8 bytes (AAPCS) */
    bic     sp, sp, #7

    /* ===== 7. Enable VFP (if any vector code generated by gcc) ===== */
    mrc     p15, 0, r1, c1, c0, 2   /* CACR */
    orr     r1, r1, #(0xf << 20)
    mcr     p15, 0, r1, c1, c0, 2
    isb
    mov     r0, #0x40000000          /* FPEXC.EN */
    vmsr    fpexc, r0

    /* ===== 8. Jump to C entry ===== */
    bl      core1_main
1:  wfe
    b       1b
    .size core1_asm_entry, . - core1_asm_entry
